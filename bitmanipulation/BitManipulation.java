package bitmanipulation;

public class BitManipulation {
    public static void main(String[] args) {
        int i = 8;
        System.out.println(~i);
        System.out.println(3 & 7);
        System.out.println(3 ^ 7);

        System.out.println(5<<10);

        System.out.println(5 & 1);
    }
}
